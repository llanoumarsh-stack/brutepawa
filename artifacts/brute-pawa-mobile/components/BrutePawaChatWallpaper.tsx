interface Props {
  style?: object;
}

export default function BrutePawaChatWallpaper({ style }: Props) {
  const { width, height } = useWindowDimensions();
  const BASE = width * 0.073;

  return (
    <View style={[StyleSheet.absoluteFill, { pointerEvents: "none" }, style as object]}>
      <LinearGradient
        colors={["#F5FAF5", "#EFF8F1", "#EAF7EC", "#E6F5E8"]}
        locations={[0, 0.3, 0.7, 1]}
        start={{ x: 0.7, y: 0 }}
        end={{ x: 0.2, y: 1 }}
        style={StyleSheet.absoluteFill}
      />
      <Svg
        width={width}
        height={height}
        viewBox={`0 0 ${width} ${height}`}
        style={StyleSheet.absoluteFill}
      >
        {ICONS_LAYOUT.map(([xPct, yPct, key, scale], idx) => {
          const size = BASE * scale;
          const x = (xPct / 100) * width - size / 2;
          const y = (yPct / 100) * height - size / 2;
          return (
            <G key={idx} transform={`translate(${x},${y}) scale(${size / 24})`}>
              <Path d={P[key]} fill={ICON_COLOR} opacity={ICON_OPACITY} />
            </G>
          );
        })}
      </Svg>
    </View>
  );
}
